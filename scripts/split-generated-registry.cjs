#!/usr/bin/env node
/* global process, require */
const fs = require("node:fs");
const path = require("node:path");

const [input, outputDirectory] = process.argv.slice(2);
if (!input || !outputDirectory) {
  throw new Error("usage: split-generated-registry.cjs <legacy-generated-file> <output-directory>");
}

const source = fs.readFileSync(input, "utf8");
const importsStart = source.indexOf("import { Registry }");
const namespacesStart = source.indexOf("export * from './cosmos-models';");
const registryStart = source.indexOf("export const registry = new Registry();");
const metadataStart = source.indexOf("/* \nKey in TxTypes");
const convenienceStart = source.indexOf("// Exported for convenience");
const eipStart = source.indexOf("/* \nEIP712Types mapping");
if ([importsStart, namespacesStart, registryStart, metadataStart, convenienceStart, eipStart].some((offset) => offset < 0)) {
  throw new Error("generated registry format changed; update the deterministic splitter");
}
for (const marker of ["import { Registry }", "export * from './cosmos-models';", "export const registry = new Registry();", "/* \nKey in TxTypes", "// Exported for convenience", "/* \nEIP712Types mapping"]) {
  if (source.split(marker).length !== 2) {
    throw new Error(`generated registry marker must occur exactly once: ${marker}`);
  }
}

const header = "// GENERATED by scripts/generate-registry.sh; DO NOT EDIT.\n";
const clean = (value) => value.split("\n").map((line) => line.trimEnd()).join("\n").trim();
const imports = clean(source.slice(importsStart, namespacesStart));
const namespaceSource = clean(source
  .slice(namespacesStart, registryStart)
  .replace(/^import \* as (Carbon|PolyNetwork).*\n/gm, ""));
const registrations = clean(source
  .slice(registryStart, metadataStart)
  .replace("export const registry = new Registry();", "const registry = new Registry();"));
const txTypes = clean(source.slice(metadataStart, convenienceStart));
const convenience = clean(source.slice(convenienceStart, eipStart));
const eipTypes = clean(source.slice(eipStart));

const registryImports = `${imports}\nimport * as Carbon from './carbon-models';\nimport * as PolyNetwork from './polynetwork-models';`;
const factory = `${header}${registryImports}\n\nexport function createRegistry() {\n${registrations.split("\n").map((line) => line ? `  ${line}` : "").join("\n")}\n  return registry;\n}\n`;
const namespaces = `${header}${namespaceSource}\n`;
const metadata = `${header}${txTypes}\n\n${eipTypes}\n`;
const singleton = `${header}import { createRegistry } from "./create-registry";\n\nexport { createRegistry };\nexport const registry = createRegistry();\n`;
const index = `${header}export * from "./model-namespaces";\nexport * from "./message-metadata";\nexport { registry } from "./registry";\n\n${convenience}\n`;

fs.mkdirSync(outputDirectory, { recursive: true });
for (const [file, contents] of Object.entries({
  "model-namespaces.ts": namespaces,
  "message-metadata.ts": metadata,
  "create-registry.ts": factory,
  "registry.ts": singleton,
  "index.ts": index,
})) {
  const target = path.join(outputDirectory, file);
  const temporary = `${target}.tmp`;
  fs.writeFileSync(temporary, contents);
  fs.renameSync(temporary, target);
}
